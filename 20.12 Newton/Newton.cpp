#include <iostream>
#include <cmath>
using namespace std;

//??????? ??????? f(x)
double f(double x) {
    //????? ????? ???????? ???????
    return x * x;   
}

int main() {
    //???? a, b, ? 
    double a, b, eps;
    cin >> a >> b >> eps;

    //????????? ?????????????
    double I0 = 0;          
    double I1 = 1e9;        
    int n = 5;              

    //???????? ???? ?????????
    while (fabs(I1 - I0) > eps) {
       
        n = 2 * n;

        I0 = I1;

        double h = (b - a) / n;

        I1 = 0;

        //???? ????????????
        for (int i = 0; i <= n - 1; i++) {

            double x = a + i * h;

            I1 += f(x);
        }

        I1 = I1 * h;
    }

    cout << I1 << endl;

    return 0;
}
