#include <iostream>
#include <cmath>

using namespace std;

// Функция, которую мы интегрируем
double f(double x) {
    // Здесь можно изменить функцию
    return sin(x);
}

int main() {
    double a, b;  //границы интегрирования
    int n;        //количество отрезков
    double S;     //результат (площадь)

    //Ввод данных
    cout << "Введите начало интервала (a): ";
    cin >> a;
    cout << "Введите конец интервала (b): ";
    cin >> b;
    cout << "Введите количество отрезков (n): ";
    cin >> n;

    //Проверка корректности ввода
    if (n <= 0) {
        cout << "Ошибка: количество отрезков должно быть положительным!" << endl;
        return 1;
    }

    if (b <= a) {
        cout << "Ошибка: конец интервала должен быть больше начала!" << endl;
        return 1;
    }

    //Инициализация
    S = 0;
    double h = (b - a) / n;
    double x = a + h;  

    //Суммирование значений функции во внутренних точках
    while (x < b) {
        double F = f(x);
        S += F;
        x += h;
    }

    //Добавление значений на границах
    double Fa = f(a);
    double Fb = f(b);
    S = (h / 2) * (Fa + Fb + 2 * S);

    cout << "Результат интегрирования (площадь под кривой): " << S << endl;

    return 0;
}